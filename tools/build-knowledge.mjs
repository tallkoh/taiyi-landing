#!/usr/bin/env node
// Slim down ../taiyi-knowledge into a bundle small enough to ship with the
// serverless functions but rich enough to ground letter generation.
//
// Usage:  bun run build:knowledge   (or: node tools/build-knowledge.mjs)
//
// Inputs:
//   ../taiyi-knowledge/taiyi-knowledge/reference/notes.jsonl
//   ../taiyi-knowledge/taiyi-knowledge/knowledge/subject.md
//   ../taiyi-knowledge/taiyi-knowledge/wiki/corpus-map.md
//
// Outputs:
//   lib/knowledge/notes.json   — filtered, slimmed teaching notes
//   lib/knowledge/digest.md    — corpus overview for system-prompt grounding

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const REPO = resolve(HERE, '..');
const KB   = resolve(REPO, '../taiyi-knowledge/taiyi-knowledge');

if (!existsSync(KB)) {
  console.error(`taiyi-knowledge corpus not found at ${KB}`);
  console.error('Clone it as a sibling directory before building knowledge.');
  process.exit(1);
}

const NOTES_IN  = resolve(KB, 'reference/notes.jsonl');
const SUBJECT   = resolve(KB, 'knowledge/subject.md');
const CORPUSMAP = resolve(KB, 'wiki/corpus-map.md');

const NOTES_OUT  = resolve(REPO, 'lib/knowledge/notes.ts');
const DIGEST_OUT = resolve(REPO, 'lib/knowledge/digest.ts');

// Keep notes that look like teaching content, not raw slide-title fragments.
const MIN_CONTENT_CHARS = 350;
const DOMAIN_KEYWORDS = [
  'bazi', 'four pillars', 'day master', 'stem', 'branch', 'pillar',
  'qimen', 'qi men', 'eight doors', 'gate', 'palace', 'sector',
  'feng shui', 'flying star', 'xuan kong', 'period 9', 'period 8',
  'solar term', 'jie qi', 'tong shu', 'date selection',
  'i ching', 'hexagram', 'trigram',
  'wood', 'fire', 'earth', 'metal', 'water', 'element',
  'wealth', 'relationship', 'health', 'identity', 'auspicious',
  'manifesting', 'activation', 'protection', 'forecast',
];

function noteIsHighSignal(content) {
  if (!content || content.length < MIN_CONTENT_CHARS) return false;
  const lc = content.toLowerCase();
  for (const kw of DOMAIN_KEYWORDS) if (lc.includes(kw)) return true;
  return false;
}

function trimContent(content, max = 1200) {
  if (!content) return '';
  let s = content.replace(/\s+/g, ' ').trim();
  if (s.length <= max) return s;
  return s.slice(0, max - 1) + '…';
}

// ---------- notes.json ----------
const raw = readFileSync(NOTES_IN, 'utf8').split('\n').filter(Boolean);
const kept = [];
for (const line of raw) {
  let n;
  try { n = JSON.parse(line); } catch { continue; }
  if (n.source_type !== 'notes') continue;
  if (!noteIsHighSignal(n.content)) continue;
  kept.push({
    id: n.id,
    source: n.source,
    content: trimContent(n.content),
  });
}
const notesTs = `// AUTO-GENERATED by tools/build-knowledge.mjs — do not edit by hand.
export interface Note { id: string; source: string; content: string; }
export const NOTES: Note[] = ${JSON.stringify(kept)};
`;
writeFileSync(NOTES_OUT, notesTs);
console.log(`notes.ts: kept ${kept.length} of ${raw.length} entries → ${NOTES_OUT}`);

// ---------- digest.md ----------
const subject = readFileSync(SUBJECT, 'utf8');
const corpusMap = readFileSync(CORPUSMAP, 'utf8');

// From corpus-map, pull just the totals + Top-Level Collections table.
// The full courses list is noise for prompt grounding.
const corpusTrim = corpusMap.split('\n## Course / Folder Maps')[0].trim();

const digest = `${subject.trim()}

---

${corpusTrim}
`;

const digestTs = `// AUTO-GENERATED by tools/build-knowledge.mjs — do not edit by hand.
export const DIGEST = ${JSON.stringify(digest)};
`;

writeFileSync(DIGEST_OUT, digestTs);
console.log(`digest.ts: ${digest.length} chars → ${DIGEST_OUT}`);
